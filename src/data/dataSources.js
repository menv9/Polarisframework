function inferDataQuality(source) {
  const endpoint = source.apiPath || ''
  const text = `${source.id} ${source.indicator} ${source.notes} ${source.coverage} ${endpoint}`.toLowerCase()

  if (!source.apiPath && !source.fredSeriesId) {
    return {
      dataFit: source.accessKind === 'manual' ? 'manual' : 'pending',
      dataMeasure: source.accessKind === 'manual' ? 'Input manual requerido' : 'Sin endpoint conectado',
      dataCheck: source.accessKind === 'manual'
        ? 'Dato correcto solo si lo introduces desde la fuente indicada.'
        : 'Falta conectar y validar la serie exacta.',
    }
  }

  if (endpoint.includes('/percentile/')) {
    return {
      dataFit: 'derived',
      dataMeasure: 'Percentil calculado desde serie histórica',
      dataCheck: 'Correcto si el modelo espera percentil 0-100.',
    }
  }

  if (endpoint.includes('/above-ma')) {
    return {
      dataFit: 'derived',
      dataMeasure: 'Señal sobre media móvil',
      dataCheck: 'Correcto si el modelo espera 1/0 o ABOVE/BELOW.',
    }
  }

  if (endpoint.includes('/spread') || endpoint.includes('/ratio') || source.fredYoY) {
    return {
      dataFit: 'derived',
      dataMeasure: source.fredYoY ? 'Variación YoY calculada' : 'Dato calculado desde dos series',
      dataCheck: 'Correcto si la fórmula coincide con el indicador.',
    }
  }

  if (text.includes('proxy')) {
    return {
      dataFit: 'proxy',
      dataMeasure: source.coverage || 'Proxy gratuito',
      dataCheck: 'Sirve como sustituto operativo, pero no es la fuente exacta original.',
    }
  }

  return {
    dataFit: 'exact',
    dataMeasure: source.coverage || source.indicator,
    dataCheck: 'Serie directa conectada a la fuente indicada.',
  }
}

