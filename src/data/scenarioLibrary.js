export const SCENARIO_SHOCKS = ['all', 'credit', 'sovereign', 'rates', 'policy', 'china', 'political', 'volatility', 'em', 'growth', 'inflation', 'banking', 'cycle']

export function getScenarioSummary(scenarios = SCENARIO_LIBRARY) {
  const totalSeverity = scenarios.reduce((sum, scenario) => sum + scenario.severity, 0)
  const critical = scenarios.filter(scenario => scenario.severity >= 4).length
  const modules = new Set(scenarios.flatMap(scenario => scenario.moduleChecks))
  return {
    count: scenarios.length,
    critical,
    avgSeverity: scenarios.length ? totalSeverity / scenarios.length : 0,
    modulesCovered: modules.size,
  }
}
